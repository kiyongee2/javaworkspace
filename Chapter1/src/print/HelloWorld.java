package print;

/*
    - 파일명: HelloWorld.java
    - 만든이: 김기용
    - 프로그램: Hello, World를 출력하는 프로그램
*/

public class HelloWorld {

	public static void main(String[] args) {
		// 주석 - 설명글, 메모(컴파일러가 번역하지 않음)
		// 글자 출력 - System(클래스), out(필드), print()-메서드(함수)
		System.out.println("Hello~ World!"); //println() - 줄바꿈
		System.out.print("안녕~ 세계야!");

	}

}
